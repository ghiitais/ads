
public class Message {
	private String id;
	private String idSour;
	private String idDest;
	private String text;
	private String isSent;
	
		
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdSour() {
		return idSour;
	}
	public void setIdSour(String idSour) {
		this.idSour = idSour;
	}
	public String getIdDest() {
		return idDest;
	}
	public void setIdDest(String idDest) {
		this.idDest = idDest;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getIsSent() {
		return isSent;
	}
	public void setIsSent(String isSent) {
		this.isSent = isSent;
	}
}
